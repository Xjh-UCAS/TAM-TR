class SP(nn.Module):
    def __init__(self, k=3, s=1):
        super(SP, self).__init__()
        self.m = nn.MaxPool2d(kernel_size=k, stride=s, padding=k // 2)

    def forward(self, x):
        return self.m(x)
    
class SPPELAN(nn.Module):
    # spp-elan
    def __init__(self, c1, c2, c3):  # ch_in, ch_out, number, shortcut, groups, expansion
        super(SPPELAN, self).__init__()
        self.c = c3
        self.cv1 = Conv(c1, c3, 1, 1)
        self.cv2 = SP(5)
        self.cv3 = SP(5)
        self.cv4 = SP(5)
        self.cv5 = Conv(4*c3, c2, 1, 1)

    def forward(self, x):
        y = [self.cv1(x)]
        y.extend(m(y[-1]) for m in [self.cv2, self.cv3, self.cv4])
        return self.cv5(torch.cat(y, 1))
    
class ChannelAttentionModule(nn.Module):
    def __init__(self, k=3, s=2):
        super(ChannelAttentionModule, self).__init__()
        self.s = s
        self.Maxpool = nn.MaxPool2d(kernel_size=k, stride=s, padding=k // 2)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        maxout = self.Maxpool(x)
        maxout = nn.functional.interpolate(maxout, scale_factor=self.s, mode='bilinear', align_corners=False)
        maxout = self.sigmoid(maxout)
        #print(maxout.shape)
        return maxout   
     
class SpatialAttentionModule(nn.Module):
    def __init__(self):
        super(SpatialAttentionModule, self).__init__()
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        maxout, _ = torch.max(x, dim=1, keepdim=True)
        maxout = self.sigmoid(maxout)
        return maxout    
    
class CPAM(nn.Module):
    def __init__(self, c1, c2):
        super(CPAM, self).__init__()
        self.channel_attention = ChannelAttentionModule()
        self.spatial_attention = SpatialAttentionModule()

    def forward(self, x):
        #print(x.shape)
        c = self.channel_attention(x) * x
        #print(c.shape)
        s = list(self.spatial_attention(si) * si for si in c.chunk(8, 1))
        s = torch.cat(s, 1)
        #print(s.shape)
        return s
